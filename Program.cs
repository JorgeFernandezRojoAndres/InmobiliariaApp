using InmobiliariaApp.Repository;
using Microsoft.AspNetCore.Authentication.Cookies;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.IdentityModel.Tokens;
using BCrypt.Net;
using InmobiliariaApp.Data;
using InmobiliariaApp.Helpers;
using Microsoft.Extensions.FileProviders;
using System.Text;

var builder = WebApplication.CreateBuilder(args);

// ======================================================
// üîπ Servicios principales
// ======================================================
builder.Services.AddControllersWithViews();
builder.Services.AddScoped<RepoInmueble>();
builder.Services.AddScoped<RepoPersona>();
builder.Services.AddScoped<IRepoContrato, RepoContrato>();
builder.Services.AddScoped<IRepoPago, RepoPago>();
builder.Services.AddScoped<IRepoTipoInmueble, RepoTipoInmueble>();
builder.Services.AddScoped<IRepoUsuario, RepoUsuario>();
builder.Services.AddScoped<JwtHelper>();

// ======================================================
// üîπ AUTENTICACI√ìN: Cookies (web) + JWT (API m√≥vil)
// ======================================================
builder.Services.AddAuthentication(options =>
{
    options.DefaultScheme = "Smart";
    options.DefaultAuthenticateScheme = "Smart";
    options.DefaultChallengeScheme = "Smart";
})
.AddPolicyScheme("Smart", "Smart", options =>
{
    options.ForwardDefaultSelector = context =>
        context.Request.Headers.ContainsKey("Authorization") &&
        context.Request.Headers["Authorization"].ToString().StartsWith("Bearer ", StringComparison.OrdinalIgnoreCase)
        ? JwtBearerDefaults.AuthenticationScheme
        : CookieAuthenticationDefaults.AuthenticationScheme;
})
// üî∏ JWT Bearer ‚Üí usado en /api/*
.AddJwtBearer(JwtBearerDefaults.AuthenticationScheme, options =>
{
    var key = builder.Configuration["Jwt:Key"];
    if (string.IsNullOrEmpty(key))
        throw new InvalidOperationException("‚ùå Faltan las claves JWT en appsettings.json (Jwt:Key).");

    options.RequireHttpsMetadata = false; // para pruebas locales
    options.SaveToken = true;

    options.TokenValidationParameters = new TokenValidationParameters
    {
        ValidateIssuer = true,
        ValidateAudience = true,
        ValidateLifetime = true,
        ValidateIssuerSigningKey = true,
        ValidIssuer = builder.Configuration["Jwt:Issuer"],
        ValidAudience = builder.Configuration["Jwt:Audience"],
        IssuerSigningKey = new SymmetricSecurityKey(Encoding.UTF8.GetBytes(key))
    };

    // üîç Log de errores JWT en consola
    options.Events = new JwtBearerEvents
    {
        OnAuthenticationFailed = ctx =>
        {
            Console.WriteLine($"‚ùå Error JWT: {ctx.Exception.Message}");
            return Task.CompletedTask;
        },
        OnChallenge = ctx =>
        {
            ctx.HandleResponse();
            ctx.Response.StatusCode = 401;
            return ctx.Response.WriteAsync("Unauthorized");
        }
    };
})
// üî∏ Cookies ‚Üí sigue habilitado para el panel MVC
.AddCookie(CookieAuthenticationDefaults.AuthenticationScheme, options =>
{
    options.LoginPath = "/Auth/Login";
    options.AccessDeniedPath = "/Auth/AccessDenied";
});

// ======================================================
// üîπ Autorizaci√≥n por pol√≠ticas
// ======================================================
builder.Services.AddAuthorization(options =>
{
    options.AddPolicy("Administrador", policy => policy.RequireRole("Administrador"));

    // Pol√≠tica JWT expl√≠cita para las APIs m√≥viles
    options.AddPolicy("ApiJwt", policy =>
        policy.AddAuthenticationSchemes(JwtBearerDefaults.AuthenticationScheme)
              .RequireAuthenticatedUser());
});

var app = builder.Build();

// ======================================================
// üîπ Middlewares
// ======================================================
app.UseRouting();

// üëâ Archivos est√°ticos
app.UseStaticFiles();

// üëâ Carpeta avatars
app.UseStaticFiles(new StaticFileOptions
{
    FileProvider = new PhysicalFileProvider(
        Path.Combine(Directory.GetCurrentDirectory(), "wwwroot", "avatars")),
    RequestPath = "/avatars"
});

// üëâ Autenticaci√≥n y autorizaci√≥n (orden correcto)
app.UseAuthentication();
app.UseAuthorization();

// ======================================================
// üîπ Mapear controladores y vistas
// ======================================================
app.MapControllers(); // ‚úÖ APIs
app.MapStaticAssets();

// üîπ Ruta por defecto (panel MVC)
app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Auth}/{action=Login}/{id?}")
    .WithStaticAssets();

// ======================================================
// üîπ Hash de prueba y Seeder
// ======================================================
var hash = BCrypt.Net.BCrypt.HashPassword("1234");
Console.WriteLine("Hash generado para 1234 => " + hash);

using (var scope = app.Services.CreateScope())
{
    var repoUsuario = scope.ServiceProvider.GetRequiredService<IRepoUsuario>();
    DbSeeder.Seed(repoUsuario);
}

// ======================================================
// üåê Escuchar en toda la red local
// ======================================================
app.Urls.Add("http://0.0.0.0:5027");
Console.WriteLine("üåç Servidor accesible en red local: http://0.0.0.0:5027");

